// A88: generate data/asset-recipes.json from the recipe module. PURE DATA —
// imports client/renderer/three/recipes.js, which has no three.js and no DOM,
// so none of the ally's canvas/gl/headless-three deps are needed. The Roblox
// composer (R8) + json2lua consume this committed artifact; the JS renderer
// (assets.js/props.js) builds from the same recipes module directly.
const fs = require('fs');
const path = require('path');

async function main() {
  const r = await import('../client/renderer/three/recipes.js');
  const c = await import('../client/renderer/three/unit-chrome.js');
  const out = {
    _note: 'GENERATED by tools/export-asset-recipes.js from client/renderer/three/recipes.js — do not hand-edit; run the tool. Primitive shapes: box, cyl, sphere. APPROXIMATION POINTS for non-three composers (e.g. Roblox R8): cone (N-sided; a 4-seg cone is a pyramid), dodeca (hills rock), torus (fortress) — build best-effort natively. The colorRole slots "primary"/"secondary" are injected from the civ visual at BUILD time; this data never carries a faction hex.',
    colorRoles: r.COLOR_ROLES,
    unitSilhouette: r.UNIT_SILHOUETTE,
    // type-level extra recipe appended after the body (chariot → wheels);
    // the Roblox buildUnit reads THIS mapping (#868) instead of hardcoding it
    typeExtra: c.TYPE_EXTRA,
    unitRecipes: r.UNIT_RECIPES,
    cityRecipe: r.CITY_RECIPE,
    propShapes: r.PROP_SHAPES
  };
  // a RENDER artifact, not an engine ruleset — lives in data/assets/ so the
  // top-level data/*.json cross-language checksum gate stays "engine rulesets
  // only" (architect ruling @aa8c1ef7); R8/json2lua read it from here
  const dir = path.join(__dirname, '..', 'data', 'assets');
  fs.mkdirSync(dir, { recursive: true });
  fs.writeFileSync(path.join(dir, 'asset-recipes.json'), JSON.stringify(out, null, 2) + '\n');
  console.log(`asset-recipes.json: ${Object.keys(r.UNIT_RECIPES).length} unit recipes, `
    + `${Object.keys(r.UNIT_SILHOUETTE).length} unit mappings, `
    + `${Object.keys(c.TYPE_EXTRA).length} type extras, ${Object.keys(r.PROP_SHAPES).length} prop shapes`);
}

main().catch(e => { console.error(e); process.exit(1); });
