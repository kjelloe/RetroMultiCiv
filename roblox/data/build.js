#!/usr/bin/env node
// R2+R4 converter (docs/10 §3: JSON/JS references cross into Roblox via a
// converter in this dir — never hand-copy numbers). Reads:
//   client/renderer/three/terrain.js (TERRAIN)   -> generated/TerrainPalette.luau
//   data/*.json (8 ruleset files)                -> generated/rulesets/<name>.luau
//                                                   (RAW JSON in a long string —
//                                                   json2lua.parse at runtime is
//                                                   the only parse authority, so
//                                                   ARRAY_MT/NULL semantics match
//                                                   the lune-verified tables)
//   shared/statehash.js over the same files      -> generated/RulesetHashes.luau
//                                                   (GameServer boot gate: parsed
//                                                   tables must hash identically)
// Output is COMMITTED (rojo build must be green from a clean clone);
// regenerate with `node roblox/data/build.js`; `--check` diffs instead of
// writing (check.sh gate 4 — fails when generated files drift from sources).
// The TERRAIN table is parsed textually because terrain.js imports three.js
// and touches `document`, so it cannot be require()d under Node.
'use strict';
const fs = require('fs');
const path = require('path');

const ROOT = path.resolve(__dirname, '..', '..');
const OUT = path.join(__dirname, 'generated');

function hexToRGB(hex) {
  const n = parseInt(hex.replace('#', '').replace('0x', ''), 16);
  return [(n >> 16) & 0xff, (n >> 8) & 0xff, n & 0xff];
}
const color3 = rgb => `Color3.fromRGB(${rgb[0]}, ${rgb[1]}, ${rgb[2]})`;

// --- TerrainPalette.luau ---------------------------------------------------
function buildTerrainPalette() {
  const src = fs.readFileSync(path.join(ROOT, 'client/renderer/three/terrain.js'), 'utf8');
  const table = src.match(/const TERRAIN = \{([\s\S]*?)\n\};/);
  if (!table) throw new Error('TERRAIN table not found in terrain.js');
  const lines = [];
  const entry = /^\s*(\w+):\s*\{(.*)\}/;
  for (const line of table[1].split('\n')) {
    const m = line.match(entry);
    if (!m) continue;
    const [, id, body] = m;
    const base = body.match(/base:\s*(-?[\d.]+)/)[1];
    const peak = body.match(/peak:\s*(-?[\d.]+)/)[1];
    const hexes = body.match(/0x[0-9a-fA-F]{6}/g) || [];
    const palette = hexes.map(h => color3(hexToRGB(h))).join(', ');
    lines.push(`\t${id} = { base = ${base}, peak = ${peak}, palette = { ${palette} } },`);
  }
  if (lines.length === 0) throw new Error('no TERRAIN entries parsed');
  return [
    '-- GENERATED by roblox/data/build.js from client/renderer/three/terrain.js',
    '-- (TERRAIN table). Do not hand-edit; regenerate instead (SPEC.md §4).',
    'return {',
    ...lines,
    '}',
    '',
  ].join('\n');
}

// --- rulesets (R4) ---------------------------------------------------------
// Raw JSON travels untouched inside Luau long strings; the module name is
// the data file's basename. json2lua.parse at server boot is the ONE parser.
const RULESET_FILES = ['buildings', 'civs', 'governments', 'rules', 'techs',
  'terrain', 'units', 'wonders'];

function buildRulesetModule(name) {
  const text = fs.readFileSync(path.join(ROOT, 'data', `${name}.json`), 'utf8');
  if (text.includes(']==]')) throw new Error(`${name}.json contains ']==]'`);
  return [
    `-- GENERATED by roblox/data/build.js from data/${name}.json — do not`,
    '-- hand-edit; regenerate instead (SPEC.md §4). Raw JSON: parse with',
    '-- luau/json2lua.parse (ARRAY_MT/NULL fidelity), never JSONDecode.',
    `return [==[${text}]==]`,
    '',
  ].join('\n');
}

// The boot gate: canonical hash per parsed ruleset file, computed here with
// the REAL shared/statehash.js — GameServer asserts the Studio-side parse
// hashes identically (the same cross-language contract as luau/data-hashes).
async function buildRulesetHashes() {
  const { hashState } = await import(path.join(ROOT, 'shared', 'statehash.js'));
  const lines = RULESET_FILES.map(name => {
    const value = JSON.parse(fs.readFileSync(path.join(ROOT, 'data', `${name}.json`), 'utf8'));
    return `\t${name} = "${hashState(value)}",`;
  });
  return [
    '-- GENERATED by roblox/data/build.js via shared/statehash.js — do not',
    '-- hand-edit. GameServer boot gate: json2lua.parse of each baked ruleset',
    '-- must reproduce these hashes exactly (data-crossing contract).',
    'return {',
    ...lines,
    '}',
    '',
  ].join('\n');
}

// --- main ------------------------------------------------------------------
async function main() {
  const files = {
    'TerrainPalette.luau': buildTerrainPalette(),
    'RulesetHashes.luau': await buildRulesetHashes(),
  };
  for (const name of RULESET_FILES) {
    files[path.join('rulesets', `${name}.luau`)] = buildRulesetModule(name);
  }
  const checkOnly = process.argv.includes('--check');
  let drift = false;
  fs.mkdirSync(path.join(OUT, 'rulesets'), { recursive: true });
  for (const [name, content] of Object.entries(files)) {
    const dest = path.join(OUT, name);
    if (checkOnly) {
      const current = fs.existsSync(dest) ? fs.readFileSync(dest, 'utf8') : '';
      if (current !== content) { console.log(`DRIFT ${name}`); drift = true; }
      else console.log(`OK ${name}`);
    } else {
      fs.writeFileSync(dest, content);
      console.log(`wrote ${path.relative(ROOT, dest)}`);
    }
  }
  process.exit(drift ? 1 : 0);
}
main();
