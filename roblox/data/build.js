#!/usr/bin/env node
// R2+R4 converter (docs/10 §3: JSON/JS references cross into Roblox via a
// converter in this dir — never hand-copy numbers). Reads:
//   client/renderer/three/terrain.js (TERRAIN)   -> generated/TerrainPalette.luau
//   data/*.json (8 ruleset files)                -> generated/rulesets/<name>.luau
//                                                   (RAW JSON in a long string —
//                                                   json2lua.parse at runtime is
//                                                   the only parse authority, so
//                                                   ARRAY_MT/NULL semantics match
//                                                   the lune-verified tables)
//   shared/statehash.js over the same files      -> generated/RulesetHashes.luau
//                                                   (GameServer boot gate: parsed
//                                                   tables must hash identically)
// Output is COMMITTED (rojo build must be green from a clean clone);
// regenerate with `node roblox/data/build.js`; `--check` diffs instead of
// writing (check.sh gate 4 — fails when generated files drift from sources).
// The TERRAIN table is parsed textually because terrain.js imports three.js
// and touches `document`, so it cannot be require()d under Node.
'use strict';
const fs = require('fs');
const path = require('path');

const ROOT = path.resolve(__dirname, '..', '..');
const OUT = path.join(__dirname, 'generated');

function hexToRGB(hex) {
  const n = parseInt(hex.replace('#', '').replace('0x', ''), 16);
  return [(n >> 16) & 0xff, (n >> 8) & 0xff, n & 0xff];
}
const color3 = rgb => `Color3.fromRGB(${rgb[0]}, ${rgb[1]}, ${rgb[2]})`;

// --- TerrainPalette.luau ---------------------------------------------------
function buildTerrainPalette() {
  const src = fs.readFileSync(path.join(ROOT, 'client/renderer/three/terrain.js'), 'utf8');
  const table = src.match(/const TERRAIN = \{([\s\S]*?)\n\};/);
  if (!table) throw new Error('TERRAIN table not found in terrain.js');
  const lines = [];
  const entry = /^\s*(\w+):\s*\{(.*)\}/;
  for (const line of table[1].split('\n')) {
    const m = line.match(entry);
    if (!m) continue;
    const [, id, body] = m;
    const base = body.match(/base:\s*(-?[\d.]+)/)[1];
    const peak = body.match(/peak:\s*(-?[\d.]+)/)[1];
    const hexes = body.match(/0x[0-9a-fA-F]{6}/g) || [];
    const palette = hexes.map(h => color3(hexToRGB(h))).join(', ');
    lines.push(`\t${id} = { base = ${base}, peak = ${peak}, palette = { ${palette} } },`);
  }
  if (lines.length === 0) throw new Error('no TERRAIN entries parsed');
  return [
    '-- GENERATED by roblox/data/build.js from client/renderer/three/terrain.js',
    '-- (TERRAIN table). Do not hand-edit; regenerate instead (SPEC.md §4).',
    'return {',
    ...lines,
    '}',
    '',
  ].join('\n');
}

// --- rulesets (R4) ---------------------------------------------------------
// Raw JSON travels untouched inside Luau long strings; the module name is
// the data file's basename. json2lua.parse at server boot is the ONE parser.
const RULESET_FILES = ['buildings', 'civs', 'governments', 'rules', 'techs',
  'terrain', 'units', 'wonders'];

function buildRulesetModule(name) {
  const text = fs.readFileSync(path.join(ROOT, 'data', `${name}.json`), 'utf8');
  if (text.includes(']==]')) throw new Error(`${name}.json contains ']==]'`);
  return [
    `-- GENERATED by roblox/data/build.js from data/${name}.json — do not`,
    '-- hand-edit; regenerate instead (SPEC.md §4). Raw JSON: parse with',
    '-- luau/json2lua.parse (ARRAY_MT/NULL fidelity), never JSONDecode.',
    `return [==[${text}]==]`,
    '',
  ].join('\n');
}

// R8: the A88 asset recipes travel the same way (raw JSON long string,
// json2lua at require time) — note the ruled data/assets/ SUBDIR source
// (top-level data/ stays the 8-file engine-ruleset contract).
const RECIPES_SRC = path.join(ROOT, 'data', 'assets', 'asset-recipes.json');

function buildAssetRecipesModule() {
  const text = fs.readFileSync(RECIPES_SRC, 'utf8');
  if (text.includes(']==]')) throw new Error(`asset-recipes.json contains ']==]'`);
  return [
    '-- GENERATED by roblox/data/build.js from data/assets/asset-recipes.json',
    '-- (the A88 export) — do not hand-edit; regenerate instead (SPEC.md §4).',
    '-- Raw JSON: parse with luau/json2lua.parse, never JSONDecode.',
    `return [==[${text}]==]`,
    '',
  ].join('\n');
}

// R8 keys gate (the ally's check-asset-sync idea): every units.json id has
// a silhouette entry, and every silhouette points at a real recipe.
function checkRecipeKeys() {
  const recipes = JSON.parse(fs.readFileSync(RECIPES_SRC, 'utf8'));
  const units = JSON.parse(fs.readFileSync(path.join(ROOT, 'data', 'units.json'), 'utf8'));
  const problems = [];
  for (const id of Object.keys(units)) {
    const key = recipes.unitSilhouette[id];
    if (key === undefined) problems.push(`units.json '${id}' has no unitSilhouette entry`);
    else if (recipes.unitRecipes[key] === undefined) problems.push(`silhouette '${id}' -> missing recipe '${key}'`);
  }
  for (const id of Object.keys(recipes.unitSilhouette)) {
    if (units[id] === undefined) problems.push(`unitSilhouette '${id}' is not a units.json id`);
  }
  for (const p of problems) console.log('KEYS ' + p);
  return problems.length === 0;
}

// The boot gate: canonical hash per parsed ruleset file, computed here with
// the REAL shared/statehash.js — GameServer asserts the Studio-side parse
// hashes identically (the same cross-language contract as luau/data-hashes).
// R8: assetRecipes joins the pins (AssetFactory gates its parse on it).
async function buildRulesetHashes() {
  const { hashState } = await import(path.join(ROOT, 'shared', 'statehash.js'));
  const lines = RULESET_FILES.map(name => {
    const value = JSON.parse(fs.readFileSync(path.join(ROOT, 'data', `${name}.json`), 'utf8'));
    return `\t${name} = "${hashState(value)}",`;
  });
  // recipes carry floats and non-ASCII prose, so statehash (a GAME-STATE
  // hasher) can't gate them — the pin is fnv32 over the RAW BYTES of the
  // JSON text (latin1 read = byte-faithful; the Luau side feeds its baked
  // long string to gamecode.fnv32, byte-identical by construction)
  const { fnv32 } = await import(path.join(ROOT, 'shared', 'gamecode.js'));
  const rawBytes = fs.readFileSync(RECIPES_SRC, 'latin1');
  lines.push(`\tassetRecipes = ${fnv32(rawBytes, false)},`);
  return [
    '-- GENERATED by roblox/data/build.js via shared/statehash.js — do not',
    '-- hand-edit. GameServer boot gate: json2lua.parse of each baked ruleset',
    '-- must reproduce these hashes exactly (data-crossing contract).',
    'return {',
    ...lines,
    '}',
    '',
  ].join('\n');
}

// --- age-snapshots (helper-open-decisions ruling 2; instant later-age starts) --
// data/age-snapshots/ (gitignored source, tools/bake-age-snapshots.js) -> one
// generated Luau module per baked state + the manifest, which the Roblox server
// loads instead of running the live fast-forward. GITIGNORED like their source
// (ruling 1: committing them would churn ~3MB per behavioral marker, since every
// engine-behavior change invalidates the statehash pins); the deploy bake step
// regenerates them. Written only when the baked source is present, and kept OUT
// of the gate-4 drift set — a clean clone has neither source nor generated and
// keeps the graceful live-ff fallback (correct, just not instant).
const SNAP_SRC = path.join(ROOT, 'data', 'age-snapshots');
function buildAgeSnapshots() {
  const manifestPath = path.join(SNAP_SRC, 'manifest.json');
  if (!fs.existsSync(manifestPath)) return null;
  const manifestText = fs.readFileSync(manifestPath, 'utf8');
  if (manifestText.includes(']==]')) throw new Error("age-snapshot manifest contains ']==]'");
  const manifest = JSON.parse(manifestText);
  const header = src => [
    `-- GENERATED by roblox/data/build.js from ${src} (gitignored; re-bake via`,
    '-- tools/bake-age-snapshots.js). Raw JSON: parse with luau/json2lua.parse.',
  ];
  const out = {};
  out['AgeSnapshotManifest.luau'] = [
    ...header('data/age-snapshots/manifest.json'),
    `return [==[${manifestText}]==]`, '',
  ].join('\n');
  for (const p of manifest.presets || []) {
    const stateText = fs.readFileSync(path.join(SNAP_SRC, p.file), 'utf8');
    if (stateText.includes(']==]')) throw new Error(`${p.file} contains ']==]'`);
    out[path.join('age-snapshots', `${p.name}.luau`)] = [
      ...header(`data/age-snapshots/${p.file}`),
      `return [==[${stateText}]==]`, '',
    ].join('\n');
  }
  return out;
}

// --- main ------------------------------------------------------------------
async function main() {
  if (process.argv.includes('--keys')) {
    const ok = checkRecipeKeys();
    console.log(ok ? 'recipe keys: OK' : 'recipe keys: FAIL');
    process.exit(ok ? 0 : 1);
  }
  const files = {
    'TerrainPalette.luau': buildTerrainPalette(),
    'RulesetHashes.luau': await buildRulesetHashes(),
    'AssetRecipes.luau': buildAssetRecipesModule(),
  };
  for (const name of RULESET_FILES) {
    files[path.join('rulesets', `${name}.luau`)] = buildRulesetModule(name);
  }
  const checkOnly = process.argv.includes('--check');
  let drift = false;
  fs.mkdirSync(path.join(OUT, 'rulesets'), { recursive: true });
  for (const [name, content] of Object.entries(files)) {
    const dest = path.join(OUT, name);
    if (checkOnly) {
      const current = fs.existsSync(dest) ? fs.readFileSync(dest, 'utf8') : '';
      if (current !== content) { console.log(`DRIFT ${name}`); drift = true; }
      else console.log(`OK ${name}`);
    } else {
      fs.writeFileSync(dest, content);
      console.log(`wrote ${path.relative(ROOT, dest)}`);
    }
  }
  // age-snapshots: gitignored, regenerated when baked source exists, NOT part
  // of the gate-4 drift check (--check leaves them untouched).
  if (!checkOnly) {
    const snaps = buildAgeSnapshots();
    if (snaps !== null) {
      fs.mkdirSync(path.join(OUT, 'age-snapshots'), { recursive: true });
      for (const [name, content] of Object.entries(snaps)) {
        fs.writeFileSync(path.join(OUT, name), content);
      }
      console.log(`wrote ${Object.keys(snaps).length} age-snapshot module(s) (gitignored)`);
    } else {
      console.log('age-snapshots: no baked source (data/age-snapshots/) — skipped (live-ff fallback)');
    }
  }
  process.exit(drift ? 1 : 0);
}
main();
